// normal function

function printMyName(name) {
  console.log(name);
}
printMyName("theG");

// array function

const printMyName = (name) => {
  console.log(name);
};
printMyName("theG");

const example = () => {
  console.log("hola");
};
example();

const introduceMyself = (name, age) => {
  console.log("hola i'm ", name, ". and i'm ", age, "years old.");
};
introduceMyself("theG", 28);
